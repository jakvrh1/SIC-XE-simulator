		LDCH @NEXT
		WD DEV
		
		
		LDA NEXT
		ADD =1
		STA NEXT

		. KO JE NASLOV VECJI OD DEV VEMO DA SMO PREBRALI VSE
		COMP #DEV
		JLT LOOP
		
		. DODAMO SE NEW LINE
		LDCH NEWLINE
		WD DEV

